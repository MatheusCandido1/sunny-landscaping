<p style="font-size: 16;text-align:center;" ><strong>NOTICE TO OWNER</strong></p>
<p><span style="font-size: 12;text-align:justify">Under the Nevada Mechanic&rsquo;s Lien Law, any contractor, subcontractor, laborer, supplier, or other person or entity that helps to improve your property, but is not paid for his or her work or supplies, has a right to place lien on your home, land, or property where the work was performed and to sue you in court to obtain payment.</span></p>
<p><span style="font-size: 12;text-align:justify">This means that after a court hearing, your home, land and property could be sold by a court officer and the proceeds if the sale used to satisfy what you owe. This can happen even if you have paid your contractor in full if the contractor&rsquo;s subcontractors, laborers, or suppliers remain unpaid.</span></p>
<p><span style="font-size: 12;text-align:justify">To preserve their rights to file a claim or lien against your property, certain claimants such as subcontractors or material suppliers are each required to provide you with a document called a &ldquo;Preliminary Notice&rdquo;. Contractors and laborers who contract with owners directly do not have to provide such notice since you are aware of their existence as an owner. A preliminary notice is not a lien against your property. Its purpose is to notify you of persons or entities that may have a contractor, subcontractor, supplier, or laborer must file a mechanic&rsquo;s lien with the county recorder which then becomes a recorded lien against your property. Generally, the maximum time allowed for filing a mechanics&rsquo; lien against your property is 90 days after substantial completion of your project.</span></p>
<p><span style="font-size: 12;text-align:justify">TO INSURE EXTRA PROTECTION FOR YOURSELF AND YOUR PROPERTY, YOU MAY WISH TO TAKE ONE OR MORE OF THE FOLLOWING STEPS:</span></p>
<p><span style="font-size: 12;text-align:justify">Require that your contractor supply you with a payment and performance bond (not a license bond), which provides that the bonding company will either complete the project or pay damages up to the amount of the bond. This payment and performance bond as well as a copy of the construction of the contract amount depending on the contractor&rsquo;s bonding ability. If financial incapacity.</span></p>
<p><span style="font-size: 12;text-align:justify">Require that payments be made directly to subcontractors and material suppliers through a joint control. Funding services may be available, for a fee, in our area which will establish voucher or other means of payment to your contractor. These services may also provide you with lien waivers and other forms of protection. Any joint control agreement should include the addendum approved by the registrar.</span></p>
<p><span style="font-size: 12;text-align:justify">Issue joint checks for payment, made out to both your contractor and subcontractors or material suppliers involved in the project. The joint checks should be made payable to the persons or </span></p>

